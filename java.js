function vacina() {
    var botao = document.getElementById('1');

    if (botao.innerHTML === "Ao Nascer") {
        botao.innerHTML = "BCG HEPATITE B";
        botao.style.backgroundColor = "#052077"; // Altera a cor para verde
    } else {
        botao.innerHTML = "Ao Nascer";
        botao.style.backgroundColor = "rgb(11, 100, 128)"; // Restaura a cor original
    }
}

